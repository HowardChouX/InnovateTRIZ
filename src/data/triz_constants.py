class TRIZDataLoader:
    """
    TRIZ数据加载器（内置数据，不依赖外部文件）
    适合Android打包
    """

    def __init__(self) -> None:
        self._matrix_data: dict[tuple[str, str], list[int]] | None = None
        self._param_names: list[str] | None = None

    def get_all_params(self) -> list[str]:
        """获取所有39个工程参数（返回缓存引用，避免重复复制）"""
        if self._param_names is None:
            self._param_names = ENGINEERING_PARAMETERS  # 直接引用，不复制
        return self._param_names

    def get_contradiction_matrix(self) -> dict[tuple[str, str], list[int]]:
        """获取矛盾矩阵（返回缓存引用，避免重复复制1189条记录）"""
        if self._matrix_data is None:
            self._matrix_data = MATRIX_39  # 直接引用，不复制
        return self._matrix_data

    def get_principle_ids(self, improving: str, worsening: str) -> list[int]:
        """
        获取指定改善和恶化参数对应的发明原理编号

        Args:
            improving: 改善参数名称
            worsening: 恶化参数名称

        Returns:
            发明原理编号列表
        """
        matrix = self.get_contradiction_matrix()

        # 精确匹配
        key = (improving, worsening)
        if key in matrix:
            return matrix[key]

        # 反向匹配
        reverse_key = (worsening, improving)
        if reverse_key in matrix:
            return matrix[reverse_key]

        # 部分匹配
        for (imp, wors), principles in matrix.items():
            if imp in improving or improving in imp:
                if wors in worsening or worsening in wors:
                    return principles

        # 默认返回
        return [1, 10, 15, 19, 35]

    def get_40_principles(self) -> list[dict[str, Any]]:
        """获取40发明原理"""
        return [{"id": id, "name": name} for id, name in PRINCIPLE_NAMES.items()]


